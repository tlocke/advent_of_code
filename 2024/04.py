def part_one(ip):
    grid = [line for line in ip.splitlines()]
    total = 0
    for y, row in enumerate(grid):
        for x, c in enumerate(row):
            if c == "X":
                for inc_x in (-1, 0, 1):
                    for inc_y in (-1, 0, 1):
                        if inc_x == 0 and inc_y == 0:
                            continue
                        t = []
                        tx = x
                        ty = y
                        for _ in range(3):
                            tx += inc_x
                            ty += inc_y
                            if 0 <= tx < len(grid[0]) and 0 <= ty < len(grid):
                                t.append(grid[ty][tx])
                        if "".join(t) == "MAS":
                            total += 1

    return total


