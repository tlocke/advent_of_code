def part_two(ip):
    grid = [line for line in ip.splitlines()]
    total = 0
    MS_SET = set("MS")
    for y, row in enumerate(grid[1:-1], start=1):
        for x, c in enumerate(row[1:-1], start=1):
            if c == "A":
                line_1 = {grid[y - 1][x - 1], grid[y + 1][x + 1]}
                line_2 = {grid[y + 1][x - 1], grid[y - 1][x + 1]}
                if line_1 == MS_SET and line_2 == MS_SET:
                    total += 1

    return total


